// Merging two arrays.
var pet = ["cat","dog","horse","cow","goat","duck","hen"];
var wildAnimal = ["tiger","lion","wolf","zebra"];

var animals = pet.concat(wildAnimal);
console.log(animals);

// Merging three arrays.
var juicyFruits = ["orang","lemon","mango","watermelon",];
var sourFruits = ["lemon","raspberry","strawberry"];
var sweetFruits = ["mango","apple","papaya"];
var totalFruits = juicyFruits.concat(sourFruits,sweetFruits);
console.log(totalFruits);

