// String.length -> it returns length of string.
console.log("1) String.length Method");
var text1 = "ABCDEFGHIJKLMNOPQRSTUVWXYZ";
text1 = text1.length;
console.log(text1);

// String.slice -> it returns length of string.
console.log("2) String.slice Method with negative values");

var text2 = "Banana Apple strawberry";
text2 = text2.slice(7,13);
console.log(text2);

// String.trim -> it returns string after trimming.
console.log("String.length Method");
var text3 = "      tejashree     ";
